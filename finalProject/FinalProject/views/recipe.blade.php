        <section class="instructions">
            <h3>Instructions</h3>
            <ol>
                <li>Chop onions, pepper and garlic
                </li>
                <li>Heat oil in a large pot over medium heat; cook and stir onion, peppers and garlic until softened.
                </li>
                <li>Heat a large skillet over medium-high heat. Cook and mix beef in the skillet until browned and crumbly, 5 to 7 minutes; add Worcestershire sauce. Crumble bouillon cubes over beef and add beer. Continue to cook an additional ~3 minutes. Stir beef mixture into pepper mixture.
                </li>
                <li>Mix in crushed tomatoes, diced tomatoes, tomato paste, and wine to the beef mixture. Season with chili powder, cumin, brown sugar, pepper sauce, basil, salt, and black pepper. Bring to a boil and reduce heat to medium-low. Cover and simmer for 90-110 minutes, stirring occasionally.
                </li>
                <li>In a separate pot cook rice.
                </li>
                <li>Mix kidney beans, corn, and rice into beef and vegetables. Continue to simmer for 25-35 minutes.
                </li>
                <li>Serve, and Enjoy!
                </li>
